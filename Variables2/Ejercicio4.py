n1 = int(input("Ingrese el Primer numero: "))
n2 = int(input("Ingrese el Segundo numero: "))
iguales = n1 == n2
print(f"Los numeros son iguales: {iguales}")
distintos = n1 != n2
print(f"Los numeros son distintos: {distintos}")
n1_mayor_que_n2 = n1>n2
print(f"n1 es mayor que n2: {n1_mayor_que_n2}")
n1_menor_o_igual_n2 = n1<=n2
print(f"n1 es menor o igual que n2: {n1_menor_o_igual_n2}")